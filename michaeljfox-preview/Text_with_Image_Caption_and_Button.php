<p style="text-align: left; font-size: 15px; color: #666; line-height: 19px; font-family: 'Gill Sans','Gill Sans MT','Lucida Sans',Arial,sans-serif;">
Cognitive dysfunction (reduced ability to think and reason) is a problem many living with Parkinson's disease face every day, but it is rarely talked about.  
</p>
<p style="text-align: left; font-size: 15px; color: #666; line-height: 19px; font-family: 'Gill Sans','Gill Sans MT','Lucida Sans',Arial,sans-serif;">
Two weeks ago we shared the <a style="color: #40b3dd;" href="https://www.michaeljfox.org/foundation/news-detail.php?learning-to-laugh-and-let-go-one-couple-journey-with-parkinson-and-dementia">story of Barbara and Lee Mendel</a>, a couple facing this challenge together. After receiving a tremendous response to that message, we wanted to update you on the action we're taking to address this troubling aspect of PD.
</p>
<p style="text-align: left; font-size: 15px; color: #666; line-height: 19px; font-family: 'Gill Sans','Gill Sans MT','Lucida Sans',Arial,sans-serif;">
It is because of stories like the Mendels' that our Foundation has made cognitive dysfunction a top priority, with allocations of over $16 million to date. As in everything we do, our goal is to "build the train and lay the tracks" at the same time - developing drugs scientifically, while simultaneously shoring up infrastructure around the business and administrative aspects of bringing a new medication to market. 
</p>
<p style="text-align: left; font-size: 15px; color: #666; line-height: 19px; font-family: 'Gill Sans','Gill Sans MT','Lucida Sans',Arial,sans-serif;">
That's why we didn't just partner with pharma giant Sanofi in 2011 to launch a clinical trial of a new treatment for cognitive dysfunction in Parkinson's - we also appealed to the U.S. Food and Drug Administration to co-chair an April 2013 meeting on what it would take to achieve regulatory approval for our treatment if the trial is successful. 
</p>
<p style="text-align: left; font-size: 15px; color: #666; line-height: 19px; font-family: 'Gill Sans','Gill Sans MT','Lucida Sans',Arial,sans-serif;">
We can make real headway on this underaddressed symptom of Parkinson's. But we need your help. Your contribution enables us to drive real results for families like the Mendels by engaging academic researchers, industry partners and regulatory agencies in the conversations and initiatives that lead to tangible progress. Please make a <a style="color: #40b3dd;" href="https://www.michaeljfox.org/get-involved/donation.php?fr_id=2380">gift</a> today.
</p>
